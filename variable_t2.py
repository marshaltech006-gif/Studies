# Variables in Python
first_name = 'Marshal'
last_name = 'Tech'
city = 'Mahabubabad'
age = 20
is_married = False
skills = ['Python', 'Gaming']

personal_info = {
    'first_name': 'Marshal',
    'last_name': 'Tech',
    'city': 'Mahabubabad',
    'age': 20,
    'is_married': False,
    'skills': ['Python', 'Gaming']
}

# Print variables
print(f"First name is: {first_name}")
print(f"Last name is: {last_name}")
print(f"City is: {city}")
print(f"Age is: {age}")
print(f"Is married? {is_married}")
print(f"My skills are: {skills}")
print(f"My personal info is: {personal_info}")

